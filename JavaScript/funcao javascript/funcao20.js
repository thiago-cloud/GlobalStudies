//IIFE -> Immediately Invoked Funcion Expression
//É uma função auto invocada que não tem nome que também pode ser classificada como função anonima é que na hora da declaração voce ja invoca a função.

(function() {
    console.log("Execução imediata!")
    console.log("Foge do escopo global!")
})()

//É uma alternativa bem iteressante para fugir do escopo global

