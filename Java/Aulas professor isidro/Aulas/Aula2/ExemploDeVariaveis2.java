public class ExemploDeVariaveis2 {
    //Operações com variaveis
    public static void main(String args[]){
        float x;
        double y;
        double z;

        x = 1.56f; //para o float e necessario colocar o sufixo no final f ou F para que a varaiavel funcione
        y = 5.58;
        z = x+y;

        System.out.println("A soma dos valores x e y e igual a: "+z);
        
        //O char permite apenas um unico caractere e enquanto o string uma frase inteira
        char c;
        String s;


        c = 'c';
        s = "Solteiro";

        System.out.println("O carinha é "+c+" para casado e s para "+s);
        
    }
}
