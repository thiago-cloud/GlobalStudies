public class testeFor2 {
    public static void main(String args[]){
        for(int i=100; i>=1 ; i--){
            System.out.println(i);
        }

        for(int a = 1; a<= 100 ; a+=3 ){
            System.out.println(a);
        }
        for(int b = 100; b >= 1 ; b-=5 ){
            System.out.println(b);
        }
    }
}


