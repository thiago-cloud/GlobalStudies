public class Aula10_operadorDeAtribuição {
    public static void main(String args[]){
        int total = 10;
        total = total +3;
        System.out.println(total);

        total+=5;
        System.out.println(total);

        total*=3;
        System.out.println(total);

        total-=5;
        System.out.println(total);

        total/=7;
        System.out.println(total);
    }
    
}
